     private void addrowseventdata() {
    eventtablemodel.setRowCount(0); 
    EventDAO eventdao = new EventDAO();
    List<AYevent> ayevent =eventdao.retrieveallevents();
    for (AYevent event : ayevent) {
        eventtablemodel.addRow(new Object[]{
            event.getEventId(),
            event.getEventName(),
            event.getEventdate(),
            event.getEventdate(),
            event.getEventlocation(),
            event.getEventorganiser()
        });
    }
}
      public void refreshevent(){
       EventDAO eventdao = new EventDAO();
        List<AYevent> eventList = eventdao.retrieveallevents(); 
        DefaultTableModel eventmodel = (DefaultTableModel) eventstable.getModel();
        eventmodel.setRowCount(0); // Clear the table
        for (AYevent event : eventList) {
             eventmodel.addRow(new Object[]{event.getEventId(), event.getEventName(), event.getEventdate()
             ,event.getEventlocation(),event.getEventorganiser()});
        }
    }
