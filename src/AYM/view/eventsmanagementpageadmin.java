    private void addeventbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addeventbtnActionPerformed
        try {
            if (eventidfield.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(this, "Please enter Id!!!");
            }else if(datefield.getText().trim().isEmpty() ){
                JOptionPane.showMessageDialog(this, "Please enter date!!!");
                String date = datefield.getText();
                if (!date.matches("\\d{2}-\\d{2}-\\d{4},\\d{2}:\\d{2}")) {
                    JOptionPane.showMessageDialog(this, "Invalid date format. Please use DD-MM-YYYY,HH:MM");
                }
            }else if(locationeventfield.getText().trim().isEmpty() ){
                JOptionPane.showMessageDialog(this, "Please enter location!!");
            }
            if(eventidfield.getText().trim().length() == 3){
                AYevent theevent = new AYevent();
                theevent.setEventId(Integer.parseInt(eventidfield.getText()));
                theevent.setEventName(eventnamefield.getText());
                theevent.setEventdate(datefield.getText());
                theevent.setEventlocation(locationeventfield.getText());
                String selectedname = (String) organisercombo.getSelectedItem();
                theevent.setEventorganiser(selectedname);
                EventDAO eventdao = new EventDAO();
                int rowAffected = eventdao.createEvent(theevent);
                if (rowAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Event created successfully");
                    eventidfield.setText("");
                    eventnamefield.setText("");
                    datefield.setText("");
                    locationeventfield.setText("");
                    organisercombo.setSelectedIndex(-1);
                    refreshevent();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to create Event!!");
                }
            }else{
                JOptionPane.showMessageDialog(this, "event id must be 3 numbers!!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_addeventbtnActionPerformed

